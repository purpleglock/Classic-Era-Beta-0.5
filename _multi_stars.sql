-- ════════════════════════════════════════════════════════════
-- КРАТНЫЕ ЗВЁЗДЫ (дополнение к редактору систем)
-- stars — метаданные звёзд-компаньонов [{letter,greek,cls,name,icon,mass,sep_au,dead}];
--   null = кратность для системы ещё не рассчитывалась, [] = рассчитана, одиночная.
-- multi — галочка «показывать компаньонов» (дефолт true); false прячет их
--   в UI/на карте, но данные (stars + тела с полем star в planets) не удаляет.
-- Тела компаньонов живут в ТОМ ЖЕ planets jsonb с полем star:'B'/'C'/… и
-- обычными pid — экономика/колонизация работают с ними без изменений.
-- ════════════════════════════════════════════════════════════
alter table public.map_systems
  add column if not exists stars jsonb,
  add column if not exists multi boolean not null default true;

// ── Viewer: init & pan/zoom ────────────────────────────────────────
function bmInit(bid){
  const el=document.getElementById('bm-svg-'+bid);if(!el)return;
  let data;try{data=JSON.parse(el.getAttribute('data-bm')||'null');}catch(e){return;}
  if(!data)return;
  const wrap=document.getElementById('bm-vp-'+bid);if(!wrap)return;
  const doInit=()=>{
    const rect=wrap.getBoundingClientRect();if(!rect.width||!rect.height)return false;
    const iz=Math.min(rect.width/BM_W,rect.height/BM_H)*0.94;
    _bmStates[bid]={data,turnIdx:-1,zoom:iz,panX:0,panY:0,hoverUnit:null,selUnit:null,animating:false};
    // Centre the canvas
    _bmStates[bid].panX=(rect.width -BM_W*iz)/2;
    _bmStates[bid].panY=(rect.height-BM_H*iz)/2;
    el.innerHTML=bmRenderFull(bid,data,-1,null);
    bmApplyView(bid);bmInitPanZoom(bid);bmSidebarUpdate(bid);bmMinimapUpdate(bid);
    bmCanvasFront(bid,bmLiveUnits(bmUnitsAt(data,-1)),data.factions||[]);
    bmUpdateWatermark(bid,'Начало сражения','');
    return true;
  };
  if(!doInit()){const ro=new ResizeObserver(()=>{if(doInit())ro.disconnect();});ro.observe(wrap);setTimeout(()=>{ro.disconnect();doInit();},2500);}
}
function bmApplyView(bid){
  const st=_bmStates[bid];if(!st)return;
  const g=document.getElementById('bm-g-'+bid);
  if(g)g.setAttribute('transform',`translate(${st.panX},${st.panY}) scale(${st.zoom})`);
}

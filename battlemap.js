// ── Wiki page → battlemap unit stats mapping ──────────────────────
// Reads характеристики из полей страницы вики и применяет к боевым параметрам.
// Поддерживаемые поля страницы: hp, shields, dmg/attack/damage, range, speed,
//   crew, population, unit_type/type, ship_class.
// Если поле отсутствует — берётся значение из BM_DEFAULTS по определённому типу.
function bmWikiToUnit(page) {
  // Определяем боевой класс: сначала из явного поля, потом по ключевым словам в title/tags
  function detectType(p) {
    const raw = (p.unit_type || p.ship_class || p.type || '').toUpperCase();
    if (BM_DEFAULTS[raw]) return raw;
    const haystack = ((p.title||'') + ' ' + (p.tags||[]).join(' ')).toLowerCase();
    if (/dreadnought|дредноут/.test(haystack))  return 'DREADNOUGHT';
    if (/carrier|авианосец/.test(haystack))      return 'CARRIER';
    if (/fighter|истребитель/.test(haystack))    return 'FIGHTER';
    if (/bomber|бомбардировщик/.test(haystack))  return 'BOMBER';
    if (/command|командир/.test(haystack))       return 'COMMAND';
    if (/starbase|звёздная база/.test(haystack)) return 'STARBASE';
    if (/station|станция/.test(haystack))        return 'STATION';
    if (/planet|планета/.test(haystack))         return 'PLANET';
    if (/moon|луна/.test(haystack))              return 'MOON';
    if (/outpost|аванпост/.test(haystack))       return 'OUTPOST';
    if (/wormhole|червоточина/.test(haystack))   return 'WORMHOLE';
    if (/jumpgate|врата/.test(haystack))         return 'JUMPGATE';
    if (/asteroid|астероид/.test(haystack))      return 'ASTEROID';
    if (/reinforce|подкрепление/.test(haystack)) return 'REINFORCEMENT';
    return 'FLEET';
  }
  const type = detectType(page);
  const def = BM_DEFAULTS[type] || BM_DEFAULTS.FLEET;

  // Числовой разбор: принимает строки "120", "120 ед", "≈120" и т.п.
  const num = (v, fallback) => {
    if (v === undefined || v === null || v === '') return fallback;
    const n = parseFloat(String(v).replace(/[^0-9.]/g, ''));
    return isNaN(n) ? fallback : n;
  };

  const hp      = num(page.hp,        def.hp);
  const shields = num(page.shields,   def.shields);
  const dmg     = num(page.dmg ?? page.attack ?? page.damage, def.dmg);
  const range   = num(page.range,     def.range);
  const speed   = num(page.speed ?? page.mobility, def.speed);
  const crew    = num(page.crew,      def.crew);
  const pop     = num(page.population, def.population);

  return { type, hp, shields, dmg, range, speed, crew, population: pop };
}


// ── Auto-damage calculation ───────────────────────────────────────
function bmeDmgCalc(fromU,toU){
  // Returns {newShields, newHp, dmgApplied, isKill}
  const dmg=fromU?.dmg||25;
  let sh=toU.shields||0;
  let hp=toU.hp||0;
  let dmgApplied=dmg;
  if(sh>0){const absorbed=Math.min(sh,dmg);sh-=absorbed;const overflow=dmg-absorbed;hp-=overflow;}
  else{hp-=dmg;}
  hp=Math.max(0,hp);sh=Math.max(0,sh);
  return{newShields:sh,newHp:hp,dmgApplied,isKill:hp<=0};
}


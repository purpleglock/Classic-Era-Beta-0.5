// ── Turn transition ───────────────────────────────────────────────
function bmGoTurn(bid,newIdx){
  const st=_bmStates[bid];if(!st)return;
  if(st._raf){cancelAnimationFrame(st._raf);st._raf=null;}
  const data=st.data;
  newIdx=Math.max(-1,Math.min((data.turns||[]).length-1,newIdx));
  const prevUnits=bmUnitsAt(data,st.turnIdx);
  const nextUnits=bmUnitsAt(data,newIdx);
  const events=newIdx>=0?(data.turns[newIdx]?.events||[]):[];
  const el=document.getElementById('bm-svg-'+bid);if(!el)return;
  st.turnIdx=newIdx;st.animating=true;
  const anim={raw:0,prevUnits,nextUnits,events};
  // Duration scales with number of attack events — more fights = longer cinematic
  const atkCount=(events.filter(e=>e.type==='attack'||e.type==='capture'||e.type==='destroy')).length;
  const DUR=Math.min(6000, 2800 + atkCount * 600);
  const start=performance.now();

  // Cinematic title overlay
  const turn=(data.turns||[])[newIdx];
  if(turn)bmCineOverlay(bid,turn);

  // Casualty popups — calculate for all attack/destroy events
  const casualtyLines=[];
  events.forEach(ev=>{
    if(ev.type==='attack'||ev.type==='destroy'){
      const tgtId=ev.type==='attack'?ev.toId:ev.unitId;
      const fromU=prevUnits.find(x=>x.id===(ev.fromId||ev.unitId));
      const toU=prevUnits.find(x=>x.id===tgtId);
      if(toU){
        // Check if this attack will kill the unit (hp after attack ≤ 0)
        const isDestroyed=ev.type==='destroy'||(ev.type==='attack'&&toU.hp-(ev.dmg||0)<=0&&(toU.shields||0)===0);
        const lines=bmCalcCasualties(fromU,toU,ev.dmg||0,isDestroyed);
        casualtyLines.push(...lines);
      }
    }
  });
  if(casualtyLines.length){
    setTimeout(()=>bmCasualtyPopup(bid,casualtyLines),1400);
  }

  const tick=now=>{
    anim.raw=Math.min((now-start)/DUR,1);
    el.innerHTML=bmRenderFull(bid,data,newIdx,anim.raw<1?anim:null);
    if(anim.raw<1){st._raf=requestAnimationFrame(tick);}
    else{st.animating=false;el.innerHTML=bmRenderFull(bid,data,newIdx,null);bmMinimapUpdate(bid);
      const u2t=bmLiveUnits(bmUnitsAt(data,newIdx));bmCanvasFront(bid,u2t,data.factions||[]);
      const _turn=(data.turns||[])[newIdx];bmUpdateWatermark(bid,_turn?_turn.label:'Начало сражения',_turn?_turn.description||'':'');}
  };
  st._raf=requestAnimationFrame(tick);
  bmSidebarUpdate(bid);
  // Show dialogue
  if(turn?.dialogue?.text)setTimeout(()=>bmShowDlg(bid,turn,data),800);
  else bmCloseDlg(bid);
}


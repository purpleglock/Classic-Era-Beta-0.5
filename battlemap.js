// ── Dominant faction calculator ───────────────────────────────────
// Возвращает фракцию с наибольшим суммарным HP боевых юнитов
function bmDominantFaction(units, factions) {
  if (!factions.length) return null;
  const scores = {};
  factions.forEach(f => { scores[f.id] = 0; });
  units.forEach(u => {
    if (!BM_OBJECT_TYPES.has(u.type) && u.faction && scores[u.faction] !== undefined) {
      scores[u.faction] += (u.hp || 0);
    }
  });
  let bestId = null, bestScore = -1;
  Object.entries(scores).forEach(([id, s]) => { if (s > bestScore) { bestScore = s; bestId = id; } });
  return factions.find(f => f.id === bestId) || factions[0] || null;
}


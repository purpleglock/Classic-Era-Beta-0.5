      const srcIdx=atkEvts.findIndex(a=>a.toId===ev.unitId||a.unitId===ev.unitId);
      const dS=srcIdx>=0?atkStart2+srcIdx*atkSlot2*0.58+atkSlot2*.68:Math.max(0.4,0.35+di*0.1);

function bmCanvasFrontEl(wrap,st,bid,units,factions){
  if(!wrap)return;

  const cvId='bm-cv-'+(wrap.id||bid);
  const animKey=cvId;
  if(_bmFrontAnim[animKey]){cancelAnimationFrame(_bmFrontAnim[animKey].raf);delete _bmFrontAnim[animKey];}

  let cv=document.getElementById(cvId);
  if(!cv){
    cv=document.createElement('canvas');
    cv.id=cvId;
    cv.style.cssText='position:absolute;top:0;left:0;width:100%;height:100%;pointer-events:none;';
    wrap.appendChild(cv);
  }
  const wRect=wrap.getBoundingClientRect();
  cv.width=wRect.width||wrap.offsetWidth||800;
  cv.height=wRect.height||wrap.offsetHeight||580;
  const W=cv.width,H=cv.height;
  const ctx=cv.getContext('2d');
  ctx.clearRect(0,0,W,H);

  const zoom=st.zoom||1,panX=st.panX||0,panY=st.panY||0;

  const terr=units.filter(u=>u.faction);
  if(!terr.length)return;

  const facIds=[...new Set(terr.map(u=>u.faction))];
  if(facIds.length<2){
    const col=bmFCol(factions,facIds[0]);
    ctx.fillStyle=hexToRgba(col,0.05);ctx.fillRect(0,0,W,H);
    return;
  }

  // Voronoi grid in canvas-pixel space
  const gs=5;
  const GW=Math.ceil(W/gs),GH=Math.ceil(H/gs);
  const owner=new Int16Array(GH*GW).fill(-1);
  for(let r=0;r<GH;r++){
    const py=(r*gs+gs/2-panY)/zoom;
    for(let c=0;c<GW;c++){
      const px=(c*gs+gs/2-panX)/zoom;
      let bestD=Infinity,bestFi=-1;
      for(let i=0;i<terr.length;i++){
        const u=terr[i],d=(u.x-px)**2+(u.y-py)**2;
        if(d<bestD){bestD=d;bestFi=facIds.indexOf(u.faction);}
      }
      owner[r*GW+c]=bestFi;
    }
  }

  // 1. Territory fill
  facIds.forEach((fid,fi)=>{
    const col=bmFCol(factions,fid);
    ctx.fillStyle=hexToRgba(col,0.07);
    for(let r=0;r<GH;r++)for(let c=0;c<GW;c++)
      if(owner[r*GW+c]===fi)ctx.fillRect(c*gs,r*gs,gs,gs);
  });

  // 2. Proximity darkening near border
  ctx.fillStyle='rgba(0,0,0,0.2)';
  for(let r=0;r<GH;r++){for(let c=0;c<GW;c++){
    const me=owner[r*GW+c];if(me<0)continue;
    let near=false;
    for(let dr=-5;dr<=5&&!near;dr++)for(let dc=-5;dc<=5&&!near;dc++){
      const nr=r+dr,nc=c+dc;
      if(nr>=0&&nr<GH&&nc>=0&&nc<GW&&owner[nr*GW+nc]!==me)near=true;
    }
    if(near)ctx.fillRect(c*gs,r*gs,gs,gs);
  }}

  // 3. Border rendering disabled (removed animated outline)
}

// ── Helper: hex color + alpha → rgba string ───────────────────────
function hexToRgba(hex,a){
  // hex can be #rrggbb or any string
  const r=parseInt((hex||'#00d4ff').slice(1,3),16)||0;
  const g=parseInt((hex||'#00d4ff').slice(3,5),16)||0;
  const b=parseInt((hex||'#00d4ff').slice(5,7),16)||0;
  return`rgba(${r},${g},${b},${a})`;
}

// ── Apply events to unit state ────────────────────────────────────
function bmApplyEvents(units,events){
  let result=units.map(u=>({...u}));
  (events||[]).forEach(ev=>{
    if(ev.type==='move'){const u=result.find(x=>x.id===ev.unitId);if(u){u.x=ev.toX;u.y=ev.toY;}}
    else if(ev.type==='setHp'){const u=result.find(x=>x.id===ev.unitId);if(u){if(ev.hp!=null)u.hp=ev.hp;if(ev.shields!=null)u.shields=ev.shields;}}
    else if(ev.type==='destroy'){result=result.filter(x=>x.id!==ev.unitId);}
    else if(ev.type==='flee'){result=result.filter(x=>x.id!==ev.unitId);}
    else if(ev.type==='capture'){const u=result.find(x=>x.id===ev.unitId);if(u){u.faction=ev.byFaction;}}
    else if(ev.type==='spawn'){if(ev.unit&&!result.find(x=>x.id===ev.unit.id))result.push({...ev.unit});}
    // ram: damage/destroy handled via explicit setHp/destroy events that follow it
  });
  return result;
}
function bmUnitsAt(data,turnIdx){
  // turnIdx: -1 = initial, 0..n = after applying turns[0..turnIdx]
  let units=(data.units||[]).map(u=>({...u}));
  const turns=data.turns||[];
  for(let i=0;i<=turnIdx&&i<turns.length;i++){
    units=bmApplyEvents(units,turns[i].events||[]);
  }
  return units;
}
function bmLiveUnits(units){
  return units.filter(u=>(u.hp>0)||(u.maxHp||0)===0);
}


function bmFmtNum(n){
  if(n>=1000000)return(n/1000000).toFixed(1)+'М';
  if(n>=1000)return(n/1000).toFixed(0)+'К';
  return String(n);
}

function bmCalcCasualties(fromU,toU,dmgApplied,isDestroyed){
  // Возвращает строки для попапа
  const lines=[];
  if(!toU)return lines;
  const crew=toU.crew||0;
  const pop=toU.population||0;
  const wasShielded=((toU.shields||0)>0)&&(toU.maxShields||0)>0;
  if(isDestroyed){
    if(crew>0) lines.push(`Экипаж «${toU.name||toU.id}»: ${bmFmtNum(crew)} чел.`);
    if(pop>0)  lines.push(`Население «${toU.name||toU.id}»: ${bmFmtNum(Math.round(pop*.15+Math.random()*pop*.25))} погибших`);
  } else if(!wasShielded&&pop>0){
    // Удар по незащищённой планете
    const casualties=Math.round((dmgApplied/(toU.maxHp||1))*pop*.08);
    if(casualties>0) lines.push(`Планета «${toU.name||toU.id}»: ${bmFmtNum(casualties)} погибших`);
  }
  return lines;
}


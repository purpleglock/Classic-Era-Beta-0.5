// ── Unit drag → move event ────────────────────────────────────────
function bmeUnitDown(e,bid,uid2){
  if(e.button!==0)return;e.stopPropagation();
  const st=bmeState(bid),b=bmeBlock(bid);
  if(e.altKey||st._pan)return;
  const[sx,sy]=bmeSVGXY(e,bid);
  const curUnits=bmeUnitsAtSi(b,st.si);
  const u=curUnits.find(x=>x.id===uid2);if(!u)return;
  const ox=u.x-sx,oy=u.y-sy;
  const startX=u.x,startY=u.y;
  let moved=false;
  const onM=ev=>{
    const[mx,my]=bmeSVGXY(ev,bid);
    let raw={x:Math.max(BM_HEX,Math.min(BM_W-BM_HEX,mx+ox)),y:Math.max(BM_HEX,Math.min(BM_H-BM_HEX,my+oy))};
    // Enforce speed limit when editing a turn (not initial placement)
    if(st.si>=0){
      const spd=u.speed||0;
      if(spd>0){
        const dx=raw.x-startX,dy=raw.y-startY;
        const dist=Math.hypot(dx,dy);
        if(dist>spd){raw.x=startX+dx/dist*spd;raw.y=startY+dy/dist*spd;}
      }
    }
    const snapped=snapToHex(raw.x,raw.y);
    const nx=snapped.x,ny=snapped.y;
    if(Math.hypot(nx-startX,ny-startY)>5){moved=true;}
    if(!b._tmp)b._tmp={};b._tmp[uid2]={x:nx,y:ny};
    bmeTmpRedraw(bid,uid2,nx,ny);
  };
  const onU=ev=>{
    document.removeEventListener('mousemove',onM);document.removeEventListener('mouseup',onU);
    if(b._tmp)delete b._tmp[uid2];
    const[mx,my]=bmeSVGXY(ev,bid);
    let raw={x:Math.max(BM_HEX,Math.min(BM_W-BM_HEX,mx+ox)),y:Math.max(BM_HEX,Math.min(BM_H-BM_HEX,my+oy))};
    // Enforce speed limit on drop too
    if(st.si>=0){
      const spd=u.speed||0;
      if(spd>0){
        const dx=raw.x-startX,dy=raw.y-startY;
        const dist=Math.hypot(dx,dy);
        if(dist>spd){raw.x=startX+dx/dist*spd;raw.y=startY+dy/dist*spd;}
      }
    }
    const snapped=snapToHex(raw.x,raw.y);
    const nx=snapped.x,ny=snapped.y;
    if(!moved){bmeClickUnit(null,bid,uid2);bmeRedraw(bid);return;}
    if(st.si<0){const iu=(b.units||[]).find(x=>x.id===uid2);if(iu){iu.x=nx;iu.y=ny;}}
    else{
      const evts=(b.turns||[])[st.si]?.events;if(!evts)return;
      const ex=evts.find(ev=>ev.type==='move'&&ev.unitId===uid2);
      if(ex){ex.toX=nx;ex.toY=ny;}else evts.push({id:uid(),type:'move',unitId:uid2,toX:nx,toY:ny});
      bmeRefreshEvList(bid);
    }
    bmeRedraw(bid);
  };
  document.addEventListener('mousemove',onM);document.addEventListener('mouseup',onU);
}


function bmeCanvasClick(e,bid){
  const st=bmeState(bid),b=bmeBlock(bid);if(!b||st._pan||e.altKey)return;
  if(st._placing){
    const[cx2,cy2]=bmeSVGXY(e,bid);
    const snapped=snapToHex(cx2,cy2);
    const cx=snapped.x,cy=snapped.y;
    const fac=b.factions||[];
    const facId=document.getElementById('bme-ufac-'+bid)?.value||fac[0]?.id||'F1';
    
    let nu;
    if (st._placingChar) {
      // Создаём юнит из страницы вики — характеристики берутся через bmWikiToUnit
      const page  = st._placingChar;
      const stats = bmWikiToUnit(page);
      nu = {
        id:          uid(),
        name:        page.title || page.name || page.slug,
        type:        stats.type,
        faction:     facId,
        x: cx, y: cy,
        hp:          stats.hp,
        maxHp:       stats.hp,
        shields:     stats.shields,
        maxShields:  stats.shields,
        dmg:         stats.dmg,
        range:       stats.range,
        speed:       stats.speed,
        crew:        stats.crew,
        population:  stats.population,
        wikiSlug:    page.slug,      // ссылка на исходную страницу
      };
      st._placingChar = null;
    } else {
      // Размещение без выбранной вики-страницы — используем дефолты по типу FLEET
      const defs = BM_DEFAULTS.FLEET;
      nu = {id:uid(),name:'Юнит',type:'FLEET',faction:facId,x:cx,y:cy,...defs,maxHp:defs.hp,maxShields:defs.shields};
    }
    
    if(st.si<0){
      // Начальное расположение → добавляем в b.units
      if(!b.units)b.units=[];b.units.push(nu);
    } else {
      // Ход N → только событие spawn, не трогаем начальное состояние
      if((b.turns||[])[st.si])b.turns[st.si].events.push({id:uid(),type:'spawn',unit:{...nu}});
    }
    bmeRedraw(bid);bmeRefreshEvList(bid);toast('Юнит добавлен — кликни снова или отмени (Esc)','ok');
  } else {
    if(st.selUnitId){st.selUnitId=null;bmeRedraw(bid);bmeHideProps(bid);}
  }
}


// ── Editor redraw ─────────────────────────────────────────────────
function bmeRedraw(bid){
  const svg=document.getElementById('bme-canvas-'+bid);if(!svg)return;
  const b=bmeBlock(bid);if(!b)return;
  const st=bmeState(bid);const fac=b.factions||[];
  const curUnits=bmLiveUnits(bmeUnitsAtSi(b,st.si));
  const turn=(b.turns||[])[st.si];const events=st.si>=0&&turn?turn.events||[]:[];
  let h='';
  h+=`<rect width="${BM_W}" height="${BM_H}" fill="#060d18"/>`;
  // Fine dot grid for editor — 20px spacing
  for(let x=20;x<BM_W;x+=20){for(let y=20;y<BM_H;y+=20){
    const major=(x%100===0&&y%100===0);
    h+=`<circle cx="${x}" cy="${y}" r="${major?.9:.35}" fill="${major?'rgba(0,180,255,.16)':'rgba(0,180,255,.07)'}"/>`;
  }}
  if(curUnits.length)h+=bmVoronoiSVG(curUnits,fac);

  // event previews
  events.forEach(ev=>{
    if(ev.type==='move'){
      const prevU=bmeUnitsAtSi(b,st.si-1).find(x=>x.id===ev.unitId)||(b.units||[]).find(x=>x.id===ev.unitId);
      if(!prevU)return;const col=bmFCol(fac,prevU.faction);
      h+=`<line x1="${prevU.x}" y1="${prevU.y}" x2="${ev.toX}" y2="${ev.toY}" stroke="${col}" stroke-width="1.8" stroke-dasharray="8,5" opacity=".5"/>`;
      const ang=Math.atan2(ev.toY-prevU.y,ev.toX-prevU.x)*180/Math.PI;
      h+=`<polygon points="0,-4 9,0 0,4" fill="${col}" transform="translate(${ev.toX},${ev.toY}) rotate(${ang})" opacity=".5"/>`;
    } else if(ev.type==='attack'||ev.type==='capture'){
      const fromU=curUnits.find(x=>x.id===(ev.fromId||ev.attackerId))||(b.units||[]).find(x=>x.id===(ev.fromId||ev.attackerId));
      const toU=curUnits.find(x=>x.id===ev.toId||(ev.unitId&&x.id===ev.unitId));
      if(!fromU||!toU)return;const col=bmFCol(fac,fromU.faction);
      const ang=Math.atan2(toU.y-fromU.y,toU.x-fromU.x)*180/Math.PI;
      const mx=(fromU.x+toU.x)/2,my=(fromU.y+toU.y)/2;
      h+=`<line x1="${fromU.x}" y1="${fromU.y}" x2="${toU.x}" y2="${toU.y}" stroke="${col}" stroke-width="1.2" stroke-dasharray="3,5" opacity=".35"/>`;
      h+=`<polygon points="0,-3.5 8,0 0,3.5" fill="${col}" transform="translate(${mx},${my}) rotate(${ang})" opacity=".45"/>`;
      h+=`<text x="${mx}" y="${my-8}" text-anchor="middle" fill="${col}" font-size="8" font-family="Share Tech Mono,monospace" opacity=".5">${ev.type==='capture'?'⚑':ev.atkType==='missile'?'🚀':'⚡'}</text>`;
    }
  });

  // units
  h+=bmRenderUnits(bid,curUnits,fac,st.selUnitId,true);
  svg.innerHTML=h;
  // update left unit list highlight
  bmeRefreshUnitList(bid);
  // Canvas territory overlay — uses editor viewport
  bmCanvasFrontEl(document.getElementById('bme-vp-'+bid),st,bid,curUnits,fac);
}

